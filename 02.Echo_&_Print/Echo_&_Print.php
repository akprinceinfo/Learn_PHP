
<!-- পিএইচপি (PHP) তে `echo` এবং `print` হল দুটি বেসিক আউটপুট স্টেটমেন্ট। এই দুটি স্টেটমেন্ট ব্যবহার করে ওয়েব পেজে ডাটা প্রদর্শন করা যায়। আসুন, দুটির বিস্তারিত জানি:

### 1. `echo`:
- `echo` হল একটি স্টেটমেন্ট, এটি কোন ফাংশন না হওয়ায় ব্র্যাকেট ব্যবহার করতে হয় না। 
- এটি দ্বারা এক বা একাধিক স্ট্রিং প্রিন্ট করা যায়।
- `echo` দ্বারা অনেকগুলি আর্গুমেন্ট পাঠানো যেতে পারে একযোগভাবে।  -->

<!-- আর্গুমেন্ট (Argument) কি? 
যখন আপনি কোন ফাংশন ডিফাইন করেন এবং সেই ফাংশনকে কল করতে চান, আপনি সেই ফাংশনে এক বা একাধিক মান পাঠাতে পারেন, যা ফাংশনের ভিতরে প্রক্রিয়া করা হয়। এই প্রেরিত মানগুলিকে আর্গুমেন্ট বলা হয়।
function sum($a, $b) {  // $a এবং $b হল দুটি আর্গুমেন্ট
    return $a + $b;
}
echo sum(5, 10);  // এখানে 5 এবং 10 হল দুটি আর্গুমেন্ট যা sum ফাংশনে প্রেরণ করা হচ্ছে -->



<!-- **উদাহরণ**: -->
<?php
echo "হ্যালো, ";
echo "বিশ্ব!";
echo "<br>";
echo "এটি", " একাধিক", " স্ট্রিং।";
?>

<!-- ### 2. `print`:
- `print` হল একটি স্টেটমেন্ট, তবে এটি একটি ভ্যালু রিটার্ন করে (সবসময় 1)।
- এটি দ্বারা শুধুমাত্র একটি স্ট্রিং প্রিন্ট করা যায়।
- `print` এ কেবল একটি আর্গুমেন্ট পাঠানো যায়।  -->

<!-- **উদাহরণ**: -->
<?php
     print "হ্যালো, বিশ্ব!";
?>

<!-- **পার্থক্য**:
- `echo` একাধিক স্ট্রিং প্রিন্ট করতে পারে কিন্তু `print` একটি স্ট্রিং প্রিন্ট করতে পারে।
- `print` একটি ভ্যালু রিটার্ন করে কিন্তু `echo` করে না।
 -->
