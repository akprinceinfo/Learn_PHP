    [বি:দ্র: echo ডবল কোটেশন এর মধ্যে কনকেট করতে হয়। সেক্ষে আমরা strtolower / strtoupper করার সময়            
    format specifier / ফরম্যাট স্পেসিফায়ার ব্যবহার করবো।] -->


                      <!-- ========================Sprintf========================== -->

    <!-- sprintf():
    sprintf() ফাংশনটি printf() এর মতই কাজ করে তবে এটি স্ট্রিং প্রিন্ট না করে স্ট্রিং রিটার্ন করে।
    এর অর্থ হল আপনি রিটার্ন স্ট্রিংটি কোন ভ্যারিয়েবলে স্টোর করতে পারেন। -->
   
    <?php
    
        $number = 9;
        $location = "ঢাকা";
        $sentence = sprintf("আমি প্রায় %d বছর ধরে %s থাকি।", $number, $location);
        echo $sentence;
    
    ?>



    
    <!-- [============================= Possible format values/specifier: ==========================] -->
        
        
            <!-- 1.%b: বাইনারি ফরম্যাট -->
            <?php 
            printf("Decimal 5 as binary: %b", 5); // Output: 101
            ?>

             <!-- 2. %c: ASCII ক্যারেক্টারের প্রতিস্থাপন -->
            <?php 
                printf("ASCII 65 represents: %c", 65); // Output: A
            ?>

           <!-- 3. %d: পূর্ণসংখ্যা প্রতিস্থাপন -->
            <?php 
                printf("Number: %d", 25); // Output: 25
            ?>

           <!-- 4. %e এবং %E: বৈজ্ঞানিক নোটেশনে সংখ্যা প্রতিস্থাপন -->
            <?php 
                printf("e: %e", 1546.98);  // 1.546980e+3
                printf("E: %E", 1546.98);  // 1.546980E+3
            ?>

            <!-- 5. %f: দশমিক সংখ্যা প্রতিস্থাপন -->
            <?php 
                printf("Decimal number: %f", 10.345); // Output: 10.345000
            ?>

            <!-- 6. %o: অক্টাল ফরম্যাটে সংখ্যা প্রতিস্থাপন -->
            <?php 
                printf("Decimal 9 as octal: %o", 9); // Output: 11
            ?>

            <!-- 7. %s: স্ট্রিং প্রতিস্থাপন -->
            <?php 
                printf("Name: %s", "Rahim"); // Output: Name: Rahim
            ?>

            <!-- 8.%u: নন-নেগেটিভ পূর্ণসংখ্যা প্রতিস্থাপন -->
            <?php 
                printf("Non-negative number: %u", -5); // Output: 4294967291 (on a 32-bit system)
            ?>

            <!-- 9.%x এবং %X: হেক্সাডেসিমাল ফরম্যাটে সংখ্যা প্রতিস্থাপন -->
            <?php
                printf("Decimal 255 as hex: %x", 255);  // Output: ff
                printf("Decimal 255 as hex: %X", 255);  // Output: FF
            ?>

        <!-- 10. %%: প্রতিশত চিহ্ন প্রতিস্থাপন -->
            <?php
                printf("Percentage: 98%%");  // Output: Percentage: 98%
            ?>

            <!-- 11. %g (ছোট অক্ষর) এবং %G (বড় অক্ষর): %f অথবা %e (%F অথবা %E জন্য) ব্যবহার করে ছোট ফরম্যাটে  
             প্রতিস্থাপন।  -->
