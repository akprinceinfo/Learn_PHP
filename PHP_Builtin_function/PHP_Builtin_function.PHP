<!-- 

           ========================PHP Builtin Function==========================

var_dump(), printf(), এবং sprintf() হল পিএইচপি ভাষায় তিনটি বিল্ট-ইন ফাংশন যা বিভিন্ন উদ্দেশ্যে ব্যবহার করা হয়।


1.var_dump():

 => এই ফাংশনটি একটি অথবা একাধিক ভ্যারিয়েবলের ডাটা টাইপ এবং মান প্রদর্শন করে।
 => এটি সাধারণত ডেভেলপমেন্টের সময় ভ্যারিয়েবলের মান এবং তার ধরণ দেখার জন্য ব্যবহার করা হয়।
    উদাহরণ: 
-->

    <?php
    $x = 5.5;
    var_dump($x);  // float(5.5)

    echo"<br/>";
    echo"<br/>";

    ?>

<!-- 
    2.printf():

    পিএইচপি তে printf() ফাংশন একটি স্ট্রিং ফরম্যাট করে প্রিন্ট করতে ব্যবহার করা হয়।
    printf() ফাংশনের মূল উদ্দেশ্য হল ভ্যারিয়েবলের মানগুলির জন্য পূর্বনির্ধারিত স্ট্রিং ফরম্যাট সেট  
    করা।

    printf() ব্যবহার করার কিছু কারণ নিম্নরূপ:

    1.ফরম্যাটেড স্ট্রিং: যদি আপনি চান যে স্ট্রিং এ কিছু স্পেসিফিক ফরম্যাটে ভ্যারিয়েবল অনুযায়ী ডাটা 
    প্রদর্শন করতে হবে, তাদি আপনি printf() ব্যবহার করতে পারেন।

    2.টাইপ স্পেসিফিক ফরম্যাটিং: আপনি চাইলে ফ্লোট নাম্বারকে কতগুলি দশমিক পর্যন্ত প্রিন্ট করতে চান তা 
    নির্ধারণ করতে পারেন, অথবা ইন্টিজারকে হেক্সাডেসিমাল, অক্টাল অথবা বাইনারি ফরম্যাটে প্রিন্ট করতে 
    পারেন।

    3.সুবিধা এবং পাঠ্যতা: বিভিন্ন ধরণের ডাটা টাইপ এবং মান ব্যবহার করার জন্য স্ট্রিং ফরম্যাট করা সহজ হয়ে 
    যায়।

-->

    <?php
        $number = 9;
        $location = "ঢাকা";
        printf("আমি প্রায় %d বছর ধরে %s থাকি।", $number, $location);
        echo"<br/>";
        echo"<br/>";

        // ====================Secend Example==========================

        $number = 10.45678;
        $name = "রাহিম";
        printf("স্বাগতম, %s! আপনি %.2f ডলার প্রদান করতে চান।", $name, $number);
        echo"<br/>";
        echo"<br/>";
    ?>

    <?php
        $fName = "Ahsanun Kabir";
        $lName = "Prince";

        printf(" Hello My Name is %s %s " , $fName,$lName); // [$fName , $lName => (আরগুমেন্ট পাঠাচ্ছে)]

        echo"<br/>";
        echo"<br/>";
    ?>
