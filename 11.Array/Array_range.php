<!-- আপনার জন্য সঠিক ফাংশন হলো range() না array_range()। এটি PHP-এ একটি মৌলিক ফাংশন যা একটি ইনটিজার সিরিজ তৈরি করতে ব্যবহৃত হয়। 

range(শুরু, শেষ, [লেভেল]);

শুরু: সিরিজ শুরু হবে এই মান থেকে।
শেষ: সিরিজ এই মান পর্যন্ত থাকবে।
লেভেল (বাছাইয়কৃত): এটি ঐচ্ছিক। এটি বলে দেয় কতগুলি সংখ্যা থাকবে একই দূরত্বে। এটি না পাওয়া গিয়ে একটি স্টেপ হিসেবে 1 ব্যবহার হবে।

-->

<?php
// 1 থেকে 5 পর্যন্ত সংখ্যা তৈরি

    $numbers = range(1, 5);
    print_r($numbers);
    
?>


<!-- এই উদাহরণে, range(1, 5) সিরিজ তৈরি করবে যা হবে [1, 2, 3, 4, 5]।

আপনি এটি অনেক উদাহরণে ব্যবহার করতে পারেন, যেমন একটি ইনডেক্স সিরিজ তৈরি করতে, একটি নির্দিষ্ট দূরত্বে সংখ্যা তৈরি করতে ইত্যাদি। -->